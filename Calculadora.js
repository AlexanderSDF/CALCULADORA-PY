/* Crea las operaciones basicas de la calculadora
SUMA-RESTA-MULTIPLICACION-DIVISION */

class Calculadora { 
  sumar(num1, num2) {
    return num1 + num2;
  }
  restar(num1, num2) {
    return num1 - num2;
  }
  multiplicar(num1, num2) {
    return num1 * num2;
  }
  dividir(num1, num2) {
    return num1 / num2;
  }
}
